2. Top 3 Products per Region

with top_products as(
  select region,
  	 product,
  	 sum(sales) as total_sales,
  	 rank() over( partition by region order by sum(sales)desc) as rank
  from sales_data 
  GROUP by region, product)
select * from top_products where rank <= 3;
